# Creating a dictionary
student = {
    "name": "Alice",
    "age": 20,
    "course": "Python"
}

# Accessing values
print("Name:", student["name"])
print("Age:", student["age"])
print("Course:", student["course"])

# Modifying values
student["age"] = 21
student["course"] = "Data Science"

print("\nAfter modification:")
print("Name:", student["name"])
print("Age:", student["age"])
print("Course:", student["course"])